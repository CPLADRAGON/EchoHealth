/* --- streaks & records --------------------------------------------------
   Personal bests derived from the already-computed daily/monthly series.
   The active-day streak counts consecutive calendar days with steps > 0.
   "Current streak" only shows under All time (it's anchored to the most
   recent recorded day, which a single-year slice would misrepresent).
   ----------------------------------------------------------------------- */
function _dayStr(d){ return d instanceof Date ? d.toISOString().slice(0,10) : String(d); }
function _streaks(days){
  // days: sorted array of "YYYY-MM-DD" strings with activity
  if (!days.length) return { longest:0, current:0, longestEnd:null };
  const ms = d => Date.parse(d + "T00:00:00Z");
  let longest = 1, run = 1, longestEnd = days[0];
  for (let i=1;i<days.length;i++){
    const gap = Math.round((ms(days[i]) - ms(days[i-1])) / 86400000);
    if (gap === 1){ run++; if (run > longest){ longest = run; longestEnd = days[i]; } }
    else if (gap > 1){ run = 1; }
  }
  // current streak: consecutive days ending on the last recorded day
  let current = 1;
  for (let i=days.length-1;i>0;i--){
    const gap = Math.round((ms(days[i]) - ms(days[i-1])) / 86400000);
    if (gap === 1) current++; else break;
  }
  return { longest, current, longestEnd };
}
function _argMax(x, y){
  let bi = -1, bv = -Infinity;
  for (let i=0;i<y.length;i++){ if (y[i] > bv){ bv = y[i]; bi = i; } }
  return bi < 0 ? null : { x: x[bi], v: bv };
}
function _argMin(x, y){
  let bi = -1, bv = Infinity;
  for (let i=0;i<y.length;i++){ if (y[i] < bv){ bv = y[i]; bi = i; } }
  return bi < 0 ? null : { x: x[bi], v: bv };
}

