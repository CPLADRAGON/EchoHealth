  if (m.total_steps!=null) lines.push("Total steps: " + m.total_steps);
  if (m.total_distance!=null) lines.push("Total walking+running distance: " + Math.round(m.total_distance) + " km");
  if (m.active_days!=null) lines.push("Active days: " + m.active_days);
  if (m.workouts!=null) lines.push("Workouts logged: " + m.workouts);
  if (m.avg_resting_hr!=null) lines.push("Average resting heart rate: " + m.avg_resting_hr + " bpm");
  if (m.latest_vo2!=null) lines.push("Latest VO2 max: " + m.latest_vo2 + " mL/kg·min" + (m.fitness_age!=null ? " (estimated fitness age " + m.fitness_age + ")" : ""));
  if (LAST_RESULT.weight && LAST_RESULT.weight.y.length){
    const wy = LAST_RESULT.weight.y;
    lines.push("Latest body weight: " + wy[wy.length-1] + " " + (m.weight_unit||"kg"));
  }
  if (LAST_RESULT.blood_pressure && LAST_RESULT.blood_pressure.x.length){
    const bp = LAST_RESULT.blood_pressure;
    const ms = Math.round(bp.sys.reduce((a,b)=>a+b,0)/bp.sys.length);
    const md = Math.round(bp.dia.reduce((a,b)=>a+b,0)/bp.dia.length);
    lines.push("Average blood pressure: " + ms + "/" + md + " mmHg");
  }
  if (LAST_RESULT.flights_month && LAST_RESULT.flights_month.y.length){
    const ft = LAST_RESULT.flights_month.y.reduce((a,b)=>a+b,0);
    lines.push("Total flights of stairs climbed: " + ft);
  }
  if (m.sleep_consistency != null) lines.push("Sleep consistency score: " + m.sleep_consistency + "/100");
  // attach the already-computed per-chart interpretations
  ["i_steps","i_dist","i_rhr","i_hrv","i_sleep","i_wo"].forEach(id=>{
    const el = document.getElementById(id);
    if (el && el.textContent.trim()) lines.push(el.textContent.trim());
  });
  // personal bests (same data shown in the Streaks & records panel)
  const recs = computeRecords(LAST_RESULT);
  if (recs.bestStepDay) lines.push("Best step day: " + recs.bestStepDay.steps + " steps on " + recs.bestStepDay.date);
  if (recs.longestStreak) lines.push("Longest active streak: " + recs.longestStreak.days + " days (ended " + recs.longestStreak.end + ")");
  if (recs.currentStreak) lines.push("Current active streak: " + recs.currentStreak.days + " days");
  if (recs.bestStepMonth) lines.push("Most steps in a month: " + recs.bestStepMonth.steps + " (" + recs.bestStepMonth.month + ")");
  if (recs.busiestWorkoutMonth) lines.push("Most workouts in a month: " + recs.busiestWorkoutMonth.count + " (" + recs.busiestWorkoutMonth.month + ")");
  if (recs.bestSleep) lines.push("Best night's sleep: " + recs.bestSleep.hours + " h on " + recs.bestSleep.date);
  if (recs.lowestRhr) lines.push("Lowest resting heart rate: " + recs.lowestRhr.bpm + " bpm on " + recs.lowestRhr.date);
  // top cross-metric patterns (associations only)
