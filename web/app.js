const JSPDF_JS = "https://cdn.jsdelivr.net/npm/jspdf@2.5.1/dist/jspdf.umd.min.js";
let _jspdfReady = null;
function loadJsPdf(){
  if (window.jspdf && window.jspdf.jsPDF) return Promise.resolve();
  if (_jspdfReady) return _jspdfReady;
  _jspdfReady = new Promise((resolve, reject)=>{
    const js = document.createElement("script");
    js.src = JSPDF_JS;
    js.onload = ()=>resolve();
    js.onerror = ()=>reject(new Error("JSPDF_LOAD"));
    document.head.appendChild(js);
  });
  return _jspdfReady;
}

