function _minMax(a){ let lo=Infinity, hi=-Infinity; for (const v of a){ if(v<lo)lo=v; if(v>hi)hi=v; } return [lo,hi]; }

// Smooth area sparkline (used for the rolling-steps trend inside the hero).
function _sparkline(ctx, vals, x, y, w, h, lineColor, fillColor){
  const n = vals.length; if (n < 2) return;
  let [lo, hi] = _minMax(vals); if (hi === lo) hi = lo + 1;
  const px = i => x + (i/(n-1))*w;
  const py = v => y + h - ((v-lo)/(hi-lo))*h;
  ctx.beginPath(); ctx.moveTo(px(0), py(vals[0]));
  for (let i=1;i<n;i++) ctx.lineTo(px(i), py(vals[i]));
  ctx.lineTo(px(n-1), y+h); ctx.lineTo(px(0), y+h); ctx.closePath();
  ctx.fillStyle = fillColor; ctx.fill();
  ctx.beginPath(); ctx.moveTo(px(0), py(vals[0]));
  for (let i=1;i<n;i++) ctx.lineTo(px(i), py(vals[i]));
  ctx.strokeStyle = lineColor; ctx.lineWidth = 4; ctx.lineJoin = "round"; ctx.stroke();
}

// Compact bar strip (used for distance-by-month seasonality).
function _miniBars(ctx, vals, x, y, w, h, color){
  const n = vals.length; if (!n) return;
  const [, hi] = _minMax(vals); const top = hi > 0 ? hi : 1;
  const gap = n > 24 ? 2 : (n > 12 ? 4 : 8);
  const bw = (w - gap*(n-1))/n;
  for (let i=0;i<n;i++){
    const bh = Math.max(3, (vals[i]/top)*h);
    _roundRect(ctx, x + i*(bw+gap), y + h - bh, bw, bh, Math.min(6, bw/2));
    ctx.fillStyle = color; ctx.fill();
  }
}

// Pick the longest-distance route in the set (the run worth showing off).
// Routes carry an accurate full-resolution `km` from the parser; fall back to
// the parser's haversine on points for any route missing it.
function _pickHeroRoute(routes){
  let best = null, bestKm = 0;
  for (const rt of routes){
    if (!rt.pts || rt.pts.length < 2) continue;
    const km = (rt.km != null) ? rt.km : routePathKm(rt.pts);
    if (km > bestKm){ bestKm = km; best = rt; }
  }
  return best ? { route: best, km: bestKm } : null;
}
// Draw a route outline, equirectangular projection, aspect-fit & centred in box.
function _drawRouteOutline(ctx, pts, x, y, w, h, color){
  const latR = pts.map(p=>p[0]), lonR = pts.map(p=>p[1]);
  const [laMin, laMax] = _minMax(latR), [loMin, loMax] = _minMax(lonR);
  const midLat = (laMin+laMax)/2 * Math.PI/180;
  // longitude degrees are narrower than latitude away from the equator
  const sx = Math.cos(midLat);
  const gw = Math.max(1e-6, (loMax-loMin)*sx), gh = Math.max(1e-6, (laMax-laMin));
  const pad = 14;
  const scale = Math.min((w-pad*2)/gw, (h-pad*2)/gh);
  const offX = x + (w - gw*scale)/2, offY = y + (h - gh*scale)/2;
  const px = lo => offX + (lo-loMin)*sx*scale;
  const py = la => offY + (laMax-la)*scale; // north up
  ctx.beginPath();
  ctx.moveTo(px(pts[0][1]), py(pts[0][0]));
  for (let i=1;i<pts.length;i++) ctx.lineTo(px(pts[i][1]), py(pts[i][0]));
  ctx.strokeStyle = color; ctx.lineWidth = 5; ctx.lineJoin = "round"; ctx.lineCap = "round"; ctx.stroke();
  // start/end dots
  ctx.fillStyle = "#34c759"; ctx.beginPath(); ctx.arc(px(pts[0][1]), py(pts[0][0]), 9, 0, 7); ctx.fill();
  const e = pts[pts.length-1];
  ctx.fillStyle = "#fa3c4c"; ctx.beginPath(); ctx.arc(px(e[1]), py(e[0]), 9, 0, 7); ctx.fill();
}

