/* =====================================================================
   pure-JavaScript streaming parser

   The parse + aggregation core (SUM_TYPES, handleTag, finalize, scanMe,
   route helpers, fitness-age + sleep-consistency math) lives in parser.js,
   loaded as a classic <script> before this one and unit-tested in Node.
   Only the browser-only streaming + unzip wrapper stays here.
   ===================================================================== */

// Stream a File (raw export.xml or export.zip) and aggregate it.
// onProgress(fraction 0..1) is called as bytes are read.
async function parseHealthExport(file, onProgress){
  const sig = new Uint8Array(await file.slice(0, 2).arrayBuffer());
  const isZip = sig[0] === 0x50 && sig[1] === 0x4B; // "PK"
  const A = newAgg();
  const td = new TextDecoder("utf-8");
  let buf = "";

  // Feed decoded text; scan complete tags; keep only the trailing partial.
  function feed(text, last){
    buf += text;
    if (!A.meScanned) scanMe(A, buf);
    if (!last){
      const lastGt = buf.lastIndexOf(">");
      if (lastGt < 0) return;
      const head = buf.slice(0, lastGt + 1);
      buf = buf.slice(lastGt + 1);
      TAG_RE.lastIndex = 0; let m;
      while ((m = TAG_RE.exec(head)) !== null) handleTag(A, m[0]);
    } else {
      TAG_RE.lastIndex = 0; let m;
      while ((m = TAG_RE.exec(buf)) !== null) handleTag(A, m[0]);
      buf = "";
    }
  }

  const total = file.size || 0;
  let read = 0;
  const reader = file.stream().getReader();

  if (isZip){
    if (!window.fflate) throw new Error("FFLATE_MISSING");
    const unzip = new fflate.Unzip();
    unzip.register(fflate.UnzipInflate);
    let picked = false, finished = false, perr = null;
    const routes = [];
    unzip.onfile = (entry) => {
      const lower = entry.name.toLowerCase();
      // Pick the first .xml that is not the CDA file. The main export dwarfs
      // everything else; this also matches localized names like 导出.xml.
      const isXml = lower.endsWith(".xml") && lower.indexOf("cda") === -1;
      if (isXml && !picked){
        picked = true;
        entry.ondata = (err, chunk, final) => {
          if (err){ perr = err; return; }
          feed(td.decode(chunk, { stream: !final }), final);
          if (final) finished = true;
        };
        entry.start();
        return;
      }
      // Collect workout route maps. Each GPX is small; we decode it, extract
      // lat/lon, decimate to a few hundred points, then discard the raw text.
      if (lower.endsWith(".gpx")){
        const dec = new TextDecoder("utf-8");
        let gbuf = "";
        entry.ondata = (err, chunk, final) => {
          if (err){ perr = err; return; }
          gbuf += dec.decode(chunk, { stream: !final });
          if (final){
            const raw = extractRoutePoints(gbuf);
            const km = Math.round(routePathKm(raw) * 100) / 100;  // full-res distance
            const dur = routeDurationSec(gbuf);                   // seconds, 0 if no timestamps
            const pts = decimate(raw, MAX_ROUTE_PTS);
            gbuf = "";
            if (pts.length >= 2){
              routes.push({ name: routeLabel(entry.name), key: routeSortKey(entry.name), pts, km, dur });
            }
          }
        };
        entry.start();
      }
    };
    while (true){
      const { value, done } = await reader.read();
      if (done){ unzip.push(new Uint8Array(0), true); break; }
      read += value.byteLength;
      unzip.push(value, false);
      if (perr) throw perr;
      if (onProgress && total) onProgress(read / total);
    }
    if (perr) throw perr;
    if (!picked) throw new Error("NO_XML");
    if (!finished) feed("", true);
    routes.sort((a, b) => a.key < b.key ? -1 : a.key > b.key ? 1 : 0);
    const result = finalize(A);
    result.routes = routes;
    return result;
  } else {
    while (true){
      const { value, done } = await reader.read();
      if (done){ feed("", true); break; }
      read += value.byteLength;
      feed(td.decode(value, { stream: true }), false);
      if (onProgress && total) onProgress(read / total);
    }
  }
  const result = finalize(A);
  result.routes = [];
  return result;
}

