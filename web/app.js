// Compute personal bests as raw structured data, shared by the records panel
// and the AI summary. Returns only the records that have data. "currentStreak"
// is included only when viewing the full timeline (a single-year slice would
// misrepresent a streak anchored to the most recent recorded day).
function computeRecords(r){
  const out = {};
  const sd = r.steps_daily;
  const activeDays = [];
  for (let i=0;i<sd.x.length;i++){ if (sd.y[i] > 0) activeDays.push(sd.x[i]); }

  const bestDay = _argMax(sd.x, sd.y);
  if (bestDay && bestDay.v > 0) out.bestStepDay = { steps: bestDay.v, date: bestDay.x };

  if (activeDays.length){
    const st = _streaks(activeDays);
    if (st.longest > 1) out.longestStreak = { days: st.longest, end: st.longestEnd || "" };
    if (LAST_RESULT === FULL_RESULT && st.current > 1) out.currentStreak = { days: st.current };
  }

  const moSteps = new Map();
  for (let i=0;i<sd.x.length;i++){ const mo = String(sd.x[i]).slice(0,7); moSteps.set(mo, (moSteps.get(mo)||0) + sd.y[i]); }
  if (moSteps.size){
    const mx = [...moSteps.keys()], my = mx.map(k=>moSteps.get(k));
    const best = _argMax(mx, my);
    if (best && best.v > 0) out.bestStepMonth = { steps: Math.round(best.v), month: best.x };
  }

  const bw = _argMax(r.workouts_month.x, r.workouts_month.y);
  if (bw && bw.v > 0) out.busiestWorkoutMonth = { count: bw.v, month: bw.x };

  const bs = _argMax(r.sleep.x, r.sleep.y);
  if (bs && bs.v > 0) out.bestSleep = { hours: Math.round(bs.v*10)/10, date: bs.x };

  const lr = _argMin(r.resting_hr.x, r.resting_hr.y);
  if (lr && isFinite(lr.v)) out.lowestRhr = { bpm: lr.v, date: lr.x };

  if (r.meta && r.meta.sleep_consistency != null) out.sleepConsistency = { score: r.meta.sleep_consistency };

  return out;
}

/* --- patterns / correlations -------------------------------------------
   Pearson correlation between day-aligned metric pairs. Conservative gates
   (>= MIN_DAYS shared days, |r| >= MIN_R) keep findings meaningful and avoid
   over-claiming. Purely derived from the existing daily series; no raw data.
   ----------------------------------------------------------------------- */
const CORR_MIN_DAYS = 30;
const CORR_MIN_R = 0.2;
// candidate pairs: [metricKeyA, seriesA, metricKeyB, seriesB] — daily series only
const CORR_PAIRS = [
  ["steps","steps_daily","sleep","sleep"],
  ["steps","steps_daily","rhr","resting_hr"],
  ["steps","steps_daily","hrv","hrv"],
  ["sleep","sleep","rhr","resting_hr"],
  ["sleep","sleep","hrv","hrv"],
  ["rhr","resting_hr","hrv","hrv"],
];
function _seriesToDayMap(s){
  // only daily series (date keys length 10) are usable for day-aligned pairing
  const m = new Map();
  for (let i=0;i<s.x.length;i++){ if (String(s.x[i]).length === 10) m.set(s.x[i], s.y[i]); }
  return m;
}
function _pearson(xs, ys){
  const n = xs.length; if (n < 2) return 0;
  let sx=0, sy=0, sxx=0, syy=0, sxy=0;
  for (let i=0;i<n;i++){ const a=xs[i], b=ys[i]; sx+=a; sy+=b; sxx+=a*a; syy+=b*b; sxy+=a*b; }
  const cov = sxy - sx*sy/n;
  const vx = sxx - sx*sx/n, vy = syy - sy*sy/n;
  if (vx <= 0 || vy <= 0) return 0;
  return cov / Math.sqrt(vx*vy);
}
function computeCorrelations(r){
  const found = [];
  const seen = new Set();
  for (const [ka, sa, kb, sb] of CORR_PAIRS){
    const key = [ka,kb].sort().join("|");
    if (seen.has(key)) continue;
    const A = r[sa], B = r[sb];
    if (!A || !B || !A.x || !B.x) continue;
    const mapA = _seriesToDayMap(A), mapB = _seriesToDayMap(B);
    if (!mapA.size || !mapB.size) continue;
    const xs = [], ys = [];
    for (const [day, va] of mapA){ if (mapB.has(day)){ xs.push(va); ys.push(mapB.get(day)); } }
    if (xs.length < CORR_MIN_DAYS) continue;
    const r0 = _pearson(xs, ys);
    if (Math.abs(r0) < CORR_MIN_R) continue;
    seen.add(key);
    found.push({ a:ka, b:kb, r:r0, n:xs.length, xs, ys });
  }
  found.sort((p,q)=> Math.abs(q.r) - Math.abs(p.r));
  return found;
}

