/* --- weekly rhythm: average steps by day of week --------------------------
   Derived from the daily step series. Monday-first labels. Returns null when
   there isn't enough data to be meaningful.
   ------------------------------------------------------------------------- */
const WEEKDAY_KEYS = ["wk.mon","wk.tue","wk.wed","wk.thu","wk.fri","wk.sat","wk.sun"];
function computeWeekday(r){
  const sd = r.steps_daily;
  if (!sd || !sd.x.length) return null;
  const sum = [0,0,0,0,0,0,0], cnt = [0,0,0,0,0,0,0];
  for (let i=0;i<sd.x.length;i++){
    const d = String(sd.x[i]); if (d.length !== 10) continue;
    // Monday-first index: JS getUTCDay() is 0=Sun..6=Sat
    const js = new Date(d + "T00:00:00Z").getUTCDay();
    const idx = (js + 6) % 7;
    sum[idx] += sd.y[i]; cnt[idx]++;
  }
  const total = cnt.reduce((a,b)=>a+b,0);
  if (total < 14) return null; // need at least a couple of weeks
  const avg = sum.map((s,i)=> cnt[i] ? s/cnt[i] : 0);
  let maxIdx = 0, minIdx = 0;
  for (let i=1;i<7;i++){
    if (avg[i] > avg[maxIdx]) maxIdx = i;
    if (cnt[i] && (avg[i] < avg[minIdx] || !cnt[minIdx])) minIdx = i;
  }
  return { avg, cnt, maxIdx, minIdx };
}

